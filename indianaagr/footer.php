	function newsletter()
	{
	 var name=$("#newsname").val();
	 var email=$("#newsemail").val();
	 
	 if(name=='' && email=='')
	 {
       // $("#error").show();
       return false;
	 }
	 //alert(name+""+email);
	 $.ajax({
		  
		  type:"POST",
		  url:"<?php echo CONFIG_PATH; ?>news_letter_save.php",
		  data:"name="+name+"&email="+email,
		  success:function(html)
		  {
		  //alert(html);
		    if(html==1)
			{
				$("#success").show();
			     $(".form-control").val("");
			}
			else if(html==2)
			{
				$("#exist").show();
			}
			else
			{
				$("#error").show();
			}	
		  }	 
		 
		 });
	
	}
</script>

